def do_twice(func):
  func()
  func()
  
def print_cross():
  print("+----", end = "")

def print_il():
  print("|    ", end = "")
  
def print_finish_1():
  do_twice(print_cross)
  print("+")

def print_finish_2():
  do_twice(print_il)
  print("|")
  
def do_four(f):
  do_twice(f)
  do_twice(f)
  
def making_frame():
  print_finish_1()
  do_four(print_finish_2)
  
def making_finish():
  do_twice(making_frame)
  print_finish_1()
  
making_finish()

